#include "3-calc.h"
/**
  * op_add - suma
  * @b: integer
  * @a: integer
  * Return: int
  */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - resta
 * @b: integer
 * @a: integer
 * Return: int
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multi
 * @b: integer
 * @a: integer
 * Return: int
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division
 * @b: integer
 * @a: integer
 * Return: int
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
  * op_mod - modulo
  * @b: integer
  * @a: integer
  * Return: int
  */
int op_mod(int a, int b)
{
		return (a % b);
}
